public class Message {

    private String msgFrom;
    private String msgTo;
    private String msgBody;
    private String msgDate;
    private String msgColor;
    private String msgFamilyFont;
    private String msgFontSize;
    private String msgFontType;

    public void setMsgFrom(String msgFrom) {
        this.msgFrom = msgFrom;
    }

    public void setMsgTo(String msgTo) {
        this.msgTo = msgTo;
    }

    public void setMsgBody(String msgBody) {
        this.msgBody = msgBody;
    }

    public void setMsgDate(String msgDate) {
        this.msgDate = msgDate;
    }

    public void setMsgColor(String msgColor) {
        this.msgColor = msgColor;
    }

    public void setMsgFamilyFont(String msgFamilyFont) {
        this.msgFamilyFont = msgFamilyFont;
    }

    public void setMsgFontSize(String msgFontSize) {
        this.msgFontSize = msgFontSize;
    }

    public void setMsgFontType(String msgFontType) {
        this.msgFontType = msgFontType;
    }

    public String getMsgFrom() {
        return msgFrom;
    }

    public String getMsgTo() {
        return msgTo;
    }

    public String getMsgBody() {
        return msgBody;
    }

    public String getMsgDate() {
        return msgDate;
    }

    public String getMsgColor() {
        return msgColor;
    }

    public String getMsgFamilyFont() {
        return msgFamilyFont;
    }

    public String getMsgFontSize() {
        return msgFontSize;
    }

    public String getMsgFontType() {
        return msgFontType;
    }
    
    
    

    
}
